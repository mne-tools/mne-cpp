void TestLsl::testResolveStreamNoMatch()
{
    stream_info outInfo("NoMatchTest", "EEG", 1, 100.0);
    stream_outlet outlet(outInfo);

    QThread::msleep(600);

    // Search for a type that doesn't match
    auto streams = resolve_stream("type", "NonExistentType_XYZ_123", 1.0);

    // Should not find our stream (different type)
    bool found = false;
    for (const auto& s : streams) {
        if (s.uid() == outlet.info().uid()) {
            found = true;
        }
    }
    QVERIFY(!found);
}

