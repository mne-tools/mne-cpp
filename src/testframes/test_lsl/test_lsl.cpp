void TestLsl::testOutletInletSingleSample()
{
    const int nChannels = 3;
    stream_info outInfo("E2E_Single", "EEG", nChannels, 100.0);
    stream_outlet outlet(outInfo);

    // Wait for the outlet to be ready
    QThread::msleep(200);

    // Create inlet from outlet's info
    stream_info resolvedInfo = outlet.info();
    resolvedInfo.set_data_host("127.0.0.1");

    stream_inlet inlet(resolvedInfo);
    inlet.open_stream();

    // Push a sample
    std::vector<float> sample = {1.5f, 2.5f, 3.5f};
    outlet.push_sample(sample);

    // Wait for data to arrive
    QThread::msleep(200);

    auto chunk = inlet.pull_chunk<float>();
    QVERIFY(!chunk.empty());

    // Verify the sample data
    bool found = false;
    for (const auto& s : chunk) {
        if (s.size() == static_cast<size_t>(nChannels) &&
            std::abs(s[0] - 1.5f) < 1e-6f &&
            std::abs(s[1] - 2.5f) < 1e-6f &&
            std::abs(s[2] - 3.5f) < 1e-6f) {
            found = true;
            break;
        }
    }
    QVERIFY(found);

    inlet.close_stream();
}

