void TestLsl::testResolveStreamsFindsOutlet()
{
    stream_info outInfo("DiscoverMe", "EEG", 4, 250.0, ChannelFormat::Float32, "disc_src");
    stream_outlet outlet(outInfo);

    QThread::msleep(600);  // Allow at least one broadcast

    auto streams = resolve_streams(1.5);

    if (streams.empty()) {
        QSKIP("Multicast discovery not available in this environment");
    }

    // Find our stream by UID
    bool found = false;
    for (const auto& s : streams) {
        if (s.uid() == outlet.info().uid()) {
            found = true;
            QCOMPARE(s.name(), std::string("DiscoverMe"));
            QCOMPARE(s.type(), std::string("EEG"));
            QCOMPARE(s.channel_count(), 4);
            QCOMPARE(s.nominal_srate(), 250.0);
            QVERIFY(s.data_port() > 0);
            QVERIFY(!s.data_host().empty());
            break;
        }
    }
    QVERIFY(found);
}

