void TestLsl::testResolveStreamsMultipleOutlets()
{
    stream_info info1("Multi1", "EEG", 1, 100.0, ChannelFormat::Float32, "m1");
    stream_info info2("Multi2", "EMG", 2, 200.0, ChannelFormat::Float32, "m2");

    stream_outlet outlet1(info1);
    stream_outlet outlet2(info2);

    QThread::msleep(600);

    auto streams = resolve_streams(1.5);

    if (streams.empty()) {
        QSKIP("Multicast discovery not available in this environment");
    }

    // Find both streams
    bool found1 = false, found2 = false;
    for (const auto& s : streams) {
        if (s.uid() == outlet1.info().uid()) found1 = true;
        if (s.uid() == outlet2.info().uid()) found2 = true;
    }
    QVERIFY(found1);
    QVERIFY(found2);
}

