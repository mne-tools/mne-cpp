void TestLsl::testOutletMultipleInlets()
{
    const int nChannels = 2;
    stream_info outInfo("E2E_MultiInlet", "EEG", nChannels, 100.0);
    stream_outlet outlet(outInfo);

    QThread::msleep(200);

    stream_info resolvedInfo = outlet.info();
    resolvedInfo.set_data_host("127.0.0.1");

    stream_inlet inlet1(resolvedInfo);
    stream_inlet inlet2(resolvedInfo);

    inlet1.open_stream();
    inlet2.open_stream();

    QThread::msleep(100);

    // Outlet should see consumers
    QVERIFY(outlet.have_consumers());

    // Push data
    outlet.push_sample({42.0f, 84.0f});

    QThread::msleep(200);

    // Both inlets should receive data
    auto chunk1 = inlet1.pull_chunk_float();
    auto chunk2 = inlet2.pull_chunk_float();

    QVERIFY(!chunk1.empty());
    QVERIFY(!chunk2.empty());

    inlet1.close_stream();
    inlet2.close_stream();
}

