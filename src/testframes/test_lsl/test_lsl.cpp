void TestLsl::testOutletInletMultipleChunks()
{
    const int nChannels = 2;
    stream_info outInfo("E2E_Multi", "EEG", nChannels, 100.0);
    stream_outlet outlet(outInfo);

    QThread::msleep(200);

    stream_info resolvedInfo = outlet.info();
    resolvedInfo.set_data_host("127.0.0.1");

    stream_inlet inlet(resolvedInfo);
    inlet.open_stream();

    // Push multiple chunks
    const int nSamples = 10;
    for (int i = 0; i < nSamples; ++i) {
        outlet.push_sample({static_cast<float>(i), static_cast<float>(i * 10)});
    }

    QThread::msleep(300);

    auto chunk = inlet.pull_chunk_float();

    // We should receive all samples (though timing may vary)
    QVERIFY(!chunk.empty());
    QVERIFY(static_cast<int>(chunk.size()) <= nSamples);

    // Verify channel count
    for (const auto& s : chunk) {
        QCOMPARE(static_cast<int>(s.size()), nChannels);
    }

    inlet.close_stream();
}

