void TestLsl::testResolveStreamsTimeout()
{
    // Short timeout with no outlets should return empty quickly
    auto start = std::chrono::steady_clock::now();
    auto streams = resolve_streams(0.1);
    auto elapsed = std::chrono::duration_cast<std::chrono::milliseconds>(
        std::chrono::steady_clock::now() - start).count();

    // Should complete within a reasonable time around the timeout
    // (some overhead is expected, but should be less than 2 seconds)
    QVERIFY(elapsed < 2000);
}

