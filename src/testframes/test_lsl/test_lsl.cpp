void TestLsl::testOutletInletLargeChunk()
{
    const int nChannels = 4;
    const int nSamples = 500;

    stream_info outInfo("E2E_Large", "EEG", nChannels, 1000.0);
    stream_outlet outlet(outInfo);

    QThread::msleep(200);

    stream_info resolvedInfo = outlet.info();
    resolvedInfo.set_data_host("127.0.0.1");
    stream_inlet inlet(resolvedInfo);
    inlet.open_stream();

    // Push a large chunk
    std::vector<std::vector<float>> bigChunk;
    bigChunk.reserve(nSamples);
    for (int i = 0; i < nSamples; ++i) {
        bigChunk.push_back({static_cast<float>(i), static_cast<float>(i * 2),
                            static_cast<float>(i * 3), static_cast<float>(i * 4)});
    }
    outlet.push_chunk(bigChunk);

    // Allow time for all data to transfer
    QThread::msleep(500);

    // Collect all received samples
    std::vector<std::vector<float>> allReceived;
    for (int attempt = 0; attempt < 5; ++attempt) {
        auto received = inlet.pull_chunk_float();
        allReceived.insert(allReceived.end(), received.begin(), received.end());
        if (static_cast<int>(allReceived.size()) >= nSamples) break;
        QThread::msleep(100);
    }

    QVERIFY(!allReceived.empty());
    // We should receive a significant portion (network timing may affect exact count)
    QVERIFY(static_cast<int>(allReceived.size()) > nSamples / 2);

    inlet.close_stream();
}

